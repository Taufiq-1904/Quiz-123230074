class Movie {
  final String title;
  final String year;
  final String genre;
  final String director;
  final String cast;
  final String rating;
  final String synopsis;
  final String image;

  Movie({
    required this.title,
    required this.year,
    required this.genre,
    required this.director,
    required this.cast,
    required this.rating,
    required this.synopsis,
    required this.image,
  });
}
